def test_create_table_sql_contains_all_required_columns_and_constraints():
    sql = _CREATE_TABLE_STAFF
    # Every column from DATA_MODEL.md §4.1.
    for col in [
        "STAFF_ID", "EMPLOYEE_ID", "FULL_NAME", "EMAIL", "DEPARTMENT",
        "JOB_TITLE", "PROCUREMENT_LEVEL", "ROLE", "IDME_VERIFIED", "ACTIVE",
        "CREATED_DATE", "UPDATED_DATE",
    ]:
        assert col in sql, f"missing column {col} in CREATE TABLE"

    # Critical constraints.
    assert "PRIMARY KEY" in sql
    assert "GENERATED BY DEFAULT AS IDENTITY" in sql
    assert "UNIQUE" in sql                          # employee_id + email
    assert "CHECK (ROLE IN ('ADMIN', 'STAFF'))" in sql
    assert "CHECK (IDME_VERIFIED IN ('Y', 'N'))" in sql
    assert "CHECK (ACTIVE IN ('Y', 'N'))" in sql
    assert "CHECK (PROCUREMENT_LEVEL BETWEEN 0 AND 3)" in sql


def test_insert_sql_uses_named_bind_variables_not_fstring():
    """AC12-D4 — INSERT must use named bind variables. Anything else is
    a SQL-injection footgun.
    """
    sql = _INSERT_STAFF
    assert "{" not in sql
    assert "%s" not in sql
    for col in ["employee_id", "full_name", "email", "role"]:
        assert f":{col}" in sql



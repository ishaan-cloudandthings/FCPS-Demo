def test_seed_has_exactly_10_rows_matching_data_model_8():
    assert len(_STAFF_SEED) == 10
    # Spot-check the headcount per level / role — matches DATA_MODEL.md §8.
    by_role = [r["role"] for r in _STAFF_SEED]
    by_level = [r["procurement_level"] for r in _STAFF_SEED]
    assert by_role.count("ADMIN") == 2
    assert by_role.count("STAFF") == 8
    assert by_level.count(3) == 2
    assert by_level.count(2) == 2
    assert by_level.count(1) == 5
    assert by_level.count(0) == 1


def test_seed_employee_ids_are_FCPS_001_to_010_in_order():
    expected = [f"FCPS-{n:03d}" for n in range(1, 11)]
    assert [r["employee_id"] for r in _STAFF_SEED] == expected


def test_email_for_uses_test_dot_com_and_lowercase():
    assert _email_for("Sarah Mitchell") == "sarah.mitchell@test.com"
    assert _email_for("Fatima Al-Hassan") == "fatima.al-hassan@test.com"


def test_bound_rows_decorate_with_email_idme_verified_active():
    rows = _bind_rows()
    assert len(rows) == 10
    for row in rows:
        assert row["email"].endswith("@test.com")
        assert "fcps" not in row["email"]
        assert row["idme_verified"] == "Y"
        assert row["active"] == "Y"



def _make_connection(*, drop_raises=None) -> MagicMock:
    """Build a mock connection whose cursor is itself a MagicMock and a
    context manager. Tracks `execute` calls in order.
    """
    cursor = MagicMock(name="cursor")
    if drop_raises is not None:
        # Only the DROP call raises; CREATE + INSERT succeed normally.
        def side_effect(sql, *args, **kwargs):
            if sql == _DROP_TABLE_STAFF:
                raise drop_raises
            return None

        cursor.execute.side_effect = side_effect

    cursor_ctx = MagicMock(name="cursor_ctx")
    cursor_ctx.__enter__.return_value = cursor
    cursor_ctx.__exit__.return_value = False

    connection = MagicMock(name="connection")
    connection.cursor.return_value = cursor_ctx
    connection._cursor = cursor
    return connection


def _ora_error(code: int) -> oracledb.DatabaseError:
    """Build a DatabaseError whose `.args[0].code == code`."""
    err = MagicMock()
    err.code = code
    return oracledb.DatabaseError(err)



def test_no_seed_row_uses_a_real_looking_spp_email():
    """Belt-and-braces — every seed email must use @test.com, never any
    spp.edu / spp.org / similar that could be mistaken for real data.
    """
    for row in _bind_rows():
        email = row["email"]
        assert email.endswith("@test.com"), f"non-test email: {email}"
        local = email.split("@")[0]
        # Local part must NOT contain the literal "spp" anywhere — extra
        # safety against accidental real-data shapes.
        assert "spp" not in local.lower(), f"spp in email local part: {email}"



def test_seed_runs_drop_create_then_10_inserts():
    conn = _make_connection()
    seed(conn)

    executed_sqls = [call.args[0] for call in conn._cursor.execute.call_args_list]

    assert executed_sqls[0] == _DROP_TABLE_STAFF
    assert executed_sqls[1] == _CREATE_TABLE_STAFF
    inserts = [sql for sql in executed_sqls[2:] if sql == _INSERT_STAFF]
    assert len(inserts) == 10
    conn.commit.assert_called_once()


def test_seed_inserts_pass_the_bound_row_dicts():
    """AC12-D4 — each INSERT call's second positional arg is the bind dict
    for that row. Spot-check the first and last rows.
    """
    conn = _make_connection()
    seed(conn)

    insert_calls = [
        call
        for call in conn._cursor.execute.call_args_list
        if call.args[0] == _INSERT_STAFF
    ]
    first = insert_calls[0].args[1]
    last = insert_calls[-1].args[1]

    assert first["employee_id"] == "EMP-001"
    assert first["full_name"] == "Sarah Mitchell"
    assert first["email"] == "sarah.mitchell@test.com"
    assert first["role"] == "PROCUREMENT_SUPERVISOR"

    assert last["employee_id"] == "EMP-010"
    assert last["role"] == "NON_STAFF"


def test_seed_swallows_ora_00942_on_first_run():
    """AC12-D2 — if STAFF doesn't exist yet, DROP raises ORA-00942 and the
    script continues to CREATE + INSERT without error.
    """
    conn = _make_connection(drop_raises=_ora_error(942))

    seed(conn)  # must not raise

    executed_sqls = [call.args[0] for call in conn._cursor.execute.call_args_list]
    # CREATE still runs (after the swallowed DROP error).
    assert _CREATE_TABLE_STAFF in executed_sqls
    inserts = [sql for sql in executed_sqls if sql == _INSERT_STAFF]
    assert len(inserts) == 10


def test_seed_propagates_other_oracle_errors():
    """AC12-D2 — ORA-00942 is the only error we swallow on DROP. Any other
    code (e.g. insufficient privileges) must propagate.
    """
    conn = _make_connection(drop_raises=_ora_error(1031))  # insufficient privileges

    with pytest.raises(oracledb.DatabaseError):
        seed(conn)


